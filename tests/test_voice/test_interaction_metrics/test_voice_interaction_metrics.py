"""
Tests for the voice interaction metrics (τ-voice panel).

Two layers:
1. Synthetic unit tests: hand-built tick patterns with known expected
   latencies, rates, and error classifications.
2. Golden parity test: a committed fixture of real (trimmed) simulations plus
   an expected-output file generated by running the ORIGINAL paper pipeline
   (src/experiments/tau_voice/exp/voice_analysis.py at 121fca0 on the
   victorb/tau-voice-paper branch) over the exact same fixture. If the ported
   module drifts from the paper's definitions, this test fails.
"""

import json
