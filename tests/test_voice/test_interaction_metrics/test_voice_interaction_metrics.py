class TestResponseEvents:
    def test_response_with_latency(self):
        # User speaks ticks 0-4, agent responds at tick 10 -> latency 1.0s
        ticks = build_ticks(
            [{"user_speech": True, "action": "keep_talking"}] * 5
            + [{}] * 5
            + [{"agent_speech": True}] * 5
            + [{}] * 5  # trailing silence so segment isn't at end
        )
        by_type = events_by_type(ticks)
        assert len(by_type["response"]) == 1
        assert by_type["response"][0].latency_sec == pytest.approx(5 * TICK)
        assert "no_response" not in by_type

    def test_no_response_when_user_speaks_again(self):
        # User speaks, agent never responds, user speaks again -> no_response
        ticks = build_ticks(
            [{"user_speech": True, "action": "keep_talking"}] * 5
            + [{}] * 10
            + [{"user_speech": True, "action": "keep_talking"}] * 5
            + [{}] * 5
            + [{"agent_speech": True}] * 3
            + [{}] * 3
        )
        by_type = events_by_type(ticks)
        assert len(by_type["no_response"]) == 1
        assert len(by_type["response"]) == 1  # second user turn gets a response


class TestYieldEvents:
    def test_yield_with_latency(self):
        # Agent speaks 0-9; user interrupts at tick 4; agent stops at tick 7
        ticks = build_ticks(
            [{"agent_speech": True}] * 4
            + [
                {"agent_speech": True, "user_speech": True, "action": "keep_talking"},
                {"agent_speech": True, "user_speech": True},
                {"agent_speech": True, "user_speech": True},
            ]
            + [{"user_speech": True}] * 3
            + [{}] * 5
            + [{"agent_speech": True}] * 3
            + [{}] * 2
        )
        by_type = events_by_type(ticks)
        assert len(by_type["yield"]) == 1
        # Agent stopped 3 ticks after the interruption started
        assert by_type["yield"][0].latency_sec == pytest.approx(3 * TICK)
        assert "no_yield" not in by_type

    def test_no_yield_when_agent_keeps_talking(self):
        # Agent speaks throughout; user interrupts at tick 2 and gives up;
        # agent never stops within the 2.0s (10-tick) window -> no_yield
        ticks = build_ticks(
            [{"agent_speech": True}] * 2
            + [{"agent_speech": True, "user_speech": True, "action": "keep_talking"}]
            * 4
            + [{"agent_speech": True}] * 12
            + [{}] * 2
        )
        by_type = events_by_type(ticks)
        assert len(by_type["no_yield"]) == 1
        assert "yield" not in by_type


class TestSelectivityEvents:
    def test_backchannel_correct_when_agent_continues(self):
        # Backchannel at tick 3 while agent talks through it (>1s window)
        ticks = build_ticks(
            [{"agent_speech": True}] * 3
            + [
                {
                    "agent_speech": True,
                    "user_speech": True,
                    "action": "backchannel",
                }
            ]
            * 2
            + [{"agent_speech": True}] * 8
            + [{}] * 2
        )
        by_type = events_by_type(ticks)
        assert len(by_type["backchannel_correct"]) == 1
        assert "backchannel_error" not in by_type

    def test_backchannel_error_when_agent_stops(self):
        # Backchannel at tick 3; agent stops 2 ticks later (within 1s window)
        ticks = build_ticks(
            [{"agent_speech": True}] * 3
            + [
                {
                    "agent_speech": True,
                    "user_speech": True,
                    "action": "backchannel",
                }
            ]
            * 2
            + [{"user_speech": False}] * 8
        )
        by_type = events_by_type(ticks)
        assert len(by_type["backchannel_error"]) == 1
        assert "backchannel_correct" not in by_type

    def test_vocal_tic_error_when_agent_yields(self):
        # Vocal tic during agent speech; agent stops within the 1s window
        ticks = build_ticks(
            [{"agent_speech": True}] * 3
            + [
                {
                    "agent_speech": True,
                    "user_speech": True,
                    "action": "keep_talking",
                    "speech_insert_type": "vocal_tic",
                }
            ]
            * 2
            + [{}] * 8
        )
        by_type = events_by_type(ticks)
        assert len(by_type["vocal_tic_error"]) == 1
        assert "vocal_tic_correct" not in by_type

    def test_non_directed_correct_when_agent_silent_stays_silent(self):
        # Out-of-turn non-directed phrase while agent is silent; agent does
        # not respond within the 2s window -> non_directed_correct
        ticks = build_ticks(
            [{"agent_speech": True}] * 3
            + [{}] * 3
            + [
                {
                    "user_speech": False,
                    "speech_insert_type": "non_directed_phrase",
                }
            ]
            * 2
            + [{}] * 15
        )
        by_type = events_by_type(ticks)
        assert len(by_type["non_directed_correct"]) == 1
        assert "non_directed_error" not in by_type


class TestPreprocessing:
    def test_filter_end_of_conversation_ticks(self):
        # 1-tick user segment at the very end (###STOP### artifact) is removed
        ticks = build_ticks(
            [{"agent_speech": True}] * 3
            + [{}]
            + [{"user_speech": True, "action": "keep_talking"}]
        )
        assert len(filter_end_of_conversation_ticks(ticks)) == len(ticks) - 1

    def test_filter_keeps_multi_tick_final_segment(self):
        ticks = build_ticks(
            [{"agent_speech": True}] * 3
            + [{"user_speech": True, "action": "keep_talking"}] * 2
        )
        assert len(filter_end_of_conversation_ticks(ticks)) == len(ticks)


class TestAggregation:
    def test_compute_metrics_nan_safe_without_effects(self):
        # Only response events; selectivity denominators are 0 -> NaN rates
        ticks = build_ticks(
            [{"user_speech": True, "action": "keep_talking"}] * 3
            + [{}] * 2
            + [{"agent_speech": True}] * 3
            + [{}] * 2
        )
        events = extract_voice_quality_events_from_simulation(ticks)
        df = compute_voice_quality_metrics(voice_quality_events_to_dataframe(events))
        row = df.iloc[0]
        assert row["response_rate"] == 1.0
        assert row["backchannel_total"] == 0
        assert math.isnan(row["backchannel_error_rate"])

    def test_raises_on_tickless_results(self):
        results = SimpleNamespace(
            simulations=[SimpleNamespace(ticks=None), SimpleNamespace(ticks=[])]
        )
        with pytest.raises(NoVoiceTicksError):
            compute_interaction_metrics_for_experiment(results)

    def test_aggregate_domain_metrics_skips_none(self):
        overall = aggregate_domain_metrics(
            {
                "retail": {
                    "response_rate": 0.9,
                    "selectivity_vocal_tic": None,
                    "counts": {"response_total": 10},
                },
                "airline": {
                    "response_rate": 0.7,
                    "selectivity_vocal_tic": 0.5,
                    "counts": {"response_total": 30},
                },
            }
        )
        assert overall["response_rate"] == pytest.approx(0.8)
        assert overall["selectivity_vocal_tic"] == pytest.approx(0.5)
        assert overall["counts"]["response_total"] == 40

    def test_aggregate_domain_metrics_empty(self):
        assert aggregate_domain_metrics({}) is None


