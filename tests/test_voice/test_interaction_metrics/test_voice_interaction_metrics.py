TESTDATA = Path(__file__).parent / "testdata"

TICK = 0.2  # seconds


def make_tick(
    tick_id: int,
    user_speech: bool = False,
    agent_speech: bool = False,
    action: str | None = None,
    speech_insert_type: str | None = None,
) -> Tick:
    """Build a minimal synthetic tick."""
    user_chunk = None
    if user_speech or speech_insert_type:
        speech_effects = None
        if speech_insert_type:
            speech_effects = SpeechEffectsResult(
                speech_insert=UserSpeechInsert(text="um", type=speech_insert_type)
            )
        user_chunk = UserMessage(
            role="user",
            content=None,
            contains_speech=user_speech,
            turn_taking_action=TurnTakingAction(action=action) if action else None,
            speech_effects=speech_effects,
        )
    agent_chunk = AssistantMessage(
        role="assistant", content=None, contains_speech=agent_speech
    )
    return Tick(
        tick_id=tick_id,
        timestamp="2026-01-01T00:00:00",
        agent_chunk=agent_chunk,
        user_chunk=user_chunk,
    )


def build_ticks(spec: list[dict]) -> list[Tick]:
    return [make_tick(i, **kwargs) for i, kwargs in enumerate(spec)]


def events_by_type(ticks):
    events = extract_voice_quality_events_from_simulation(ticks)
    by_type: dict[str, list] = {}
    for e in events:
        by_type.setdefault(e.event_type, []).append(e)
    return by_type


