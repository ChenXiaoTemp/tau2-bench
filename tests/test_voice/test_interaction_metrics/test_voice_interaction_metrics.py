class TestGoldenParity:
    """
    The fixture in testdata/experiment/ contains 3 real simulations from the
    gpt-realtime-2-minimal retail leaderboard run (trimmed of audio,
    transcripts, raw_data, and tool calls — none of which affect the metrics).
    testdata/expected_metrics.json was produced by running the ORIGINAL
    τ-voice paper pipeline (voice_analysis.py at commit 121fca0) over these
    exact files. Do not regenerate it with the ported module.
    """

    def test_matches_original_pipeline(self):
        block = compute_interaction_metrics_block(
            discover_experiment_paths([str(TESTDATA / "experiment")])
        )
        with open(TESTDATA / "expected_metrics.json") as f:
            expected = json.load(f)

        actual = block["domains"]["retail"]
        for name, expected_value in expected.items():
            if name == "counts":
                assert actual["counts"] == expected_value
            elif expected_value is None:
                assert actual[name] is None, name
            else:
                assert actual[name] == pytest.approx(expected_value, abs=1e-9), name

    def test_overall_equals_single_domain(self):
        block = compute_interaction_metrics_block(
            discover_experiment_paths([str(TESTDATA / "experiment")])
        )
        for name, value in block["domains"]["retail"].items():
            if name == "counts":
                continue
            assert block["overall"][name] == pytest.approx(value)

    def test_block_shape(self):
        block = compute_interaction_metrics_block(
            discover_experiment_paths([str(TESTDATA / "experiment")])
        )
        assert block["version"]
        assert block["config"] == InteractionMetricsConfig().as_dict()
        assert set(block["domains"]) == {"retail"}

    def test_block_validates_against_submission_schema(self):
        from tau2.scripts.leaderboard.submission import InteractionMetrics

        block = compute_interaction_metrics_block(
            discover_experiment_paths([str(TESTDATA / "experiment")])
        )
        model = InteractionMetrics.model_validate(block)
        assert model.domains["retail"].response_rate == pytest.approx(
            block["domains"]["retail"]["response_rate"]
        )
        assert (
            model.overall.counts.response_total
            == (block["overall"]["counts"]["response_total"])
        )
        # Round-trip through JSON must be lossless (submission.json format)
        rehydrated = InteractionMetrics.model_validate_json(model.model_dump_json())
        assert rehydrated == model
